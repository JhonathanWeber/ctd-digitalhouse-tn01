// Exemplos do video da aula 5 - Operadores matemáticos

// Operadores Adição

console.log(`A soma de 2 + 2 é igual: ${2 + 2}`); // -> output deverá ser 4.

// Operadores Subtração
console.log(`A subtração de 10 - 5 é igual: ${10 - 5}`); // -> output deverá ser 5.

// Operadores de Multiplicação
console.log(`A multiplicação de 10 * 5 é igual: ${10 * 5}`); // -> output deverá ser 50.

// Operadores de Divisão
console.log(`A divisão de 10 / 2 é igual: ${10 / 2}`); // -> output deverá ser 5.

// Operadores de Resto da Divisão
console.log(`O resto da divsão de 4 / 3 é igual: ${4 % 3}`); // -> output deverá ser 1.

// Operadores de Exponenciação
console.log(`A exponenciação de 3 elevado a 2 é igual: ${3 ** 2}`); // -> output deverá ser 9.

// Operadores de incremento e decremento
let num = 0;

console.log(
  `Valor da variável [num] é 0, depois do pós-incrimento é: ${num++}, depois do pré-drecemento é: ${++num}`
); // Operador de incremento acresenta 1 unidade ao valor associado
// o mesmo ocorre para o operador de decremento
